package com.domain.quiz.backend.dto;

public class CategoryDTO {
    private String id;
    private String name;

    public CategoryDTO() {}

    public CategoryDTO(String id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and Setters
    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
